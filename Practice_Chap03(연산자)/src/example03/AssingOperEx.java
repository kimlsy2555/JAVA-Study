package example03;

public class AssingOperEx {

	public static void main(String[] args) {
		// 복합 대입 연산자는 값을 누적시킬떄 많이 사용한다.
		
		int result = 0;
		
		result += 10;
		System.out.println(result);
		
		result-=5;
		System.out.println(result);
		
		result*=5;
		System.out.println(result);
		
		result/=5;
		System.out.println(result);
		
		result%=5;
		System.out.println(result);
		
		
		
	}

}
